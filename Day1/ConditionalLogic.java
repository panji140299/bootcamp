public class ConditionalLogic {
    public static void main(String[] args){
        int x = 20;
        int y = 40;

        System.out.println("X + Y = "+(x+y));
        System.out.println("X - Y = "+(x-y));
        System.out.println("X * Y = "+(x*y));
        System.out.println("X / Y = "+(x/y));
        System.out.println("X % Y = "+(x%y));
        System.out.println("X ++ = "+(x++));
        System.out.println("X -- = "+(x--));

        //perbedaan x++ dan ++x
        System.out.println("X ++ = "+(x++));
        System.out.println("++ X = "+(++x));
        

    }
}
